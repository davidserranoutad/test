#' Suma dos números
#'
#' Recibe dos números y devuelve su suma
#' @param x Primer valor numérico a sumar
#' @param y Primer valor numérico a sumar
#' @return La suma de las dos entradas
#' @examples suma.dos.numeros(1,0)
#' suma.dos.numeros(-5,3)
#' @export
#'
suma.dos.numeros <- function(x,y){

  return(x+y)

}

#' Multiplica dos números
#'
#' Recibe dos números y devuelve su producto
#' @param x Primer valor numérico a multiplicar
#' @param y Primer valor numérico a multiplicar
#' @return El producto de las dos entradas
#' @examples multiplica.dos.numeros(1,0)
#' multiplica.dos.numeros(-5,3)
#' @export
#'
multiplica.dos.numeros <- function(x,y){

  return(x*y)

}
